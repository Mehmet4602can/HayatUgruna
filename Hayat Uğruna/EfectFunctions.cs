        public static void graficHPBar(int currentHP, int maxHP)
        {
            if (currentHP < 0)
            {
                currentHP = 0;
            }
            const int barwidth = 20;
            int filledbar = (int)((currentHP / (float)maxHP) * barwidth);
            string bar = new string('█', filledbar) + new string('-', barwidth - filledbar);
            Console.WriteLine($"[{bar}] {currentHP}/{maxHP}");
        }
